#   Tip String
"""
Em Python um dado Ã© considerado String sempre que :

1) Estiver entre aspas simples --> 'uma string'
2) Estiver entre aspas duplas --> "uma string"
3) Estiver entre aspas simples triplas --> '''uma string'''
4) Estiver entre aspas duplas triplas 
"""

nome = 'Geek university'
print (nome)
print(type(nome))

nome = "Geek university"
print (nome)
print(type(nome))

nome = '''Geek university'''
print (nome)
print(type(nome))

nome = "Gina's bar"
print (nome)
print(type(nome))

nome = 'Angelina \nJolie'
print (nome)
print(type(nome))

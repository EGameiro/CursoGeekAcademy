#   Lembrar que o separador decimal é o PONTO e não a VIRGULA

valor=1, 44
print (valor)
print(type(valor))

valor = 1.44
print(valor)
print(type(valor))

#   É possivel fazer dupla atribuição
valor1, valor2 = 1, 44
print(valor1)
print(type(valor1))
print(valor2)
print(type(valor2))

# Podemos converter um Float para Int
res=int(valor)
print(res)
print(type(res))
